//addition, subtraction, multiplication, division of two number
#include<stdio.h>
int main()
{
    int a,b,c,d,e,f;
    a=24;
    b=45;
    c=a+b;
    d=a-b;
    e=a*b;
    f=a/b;
    printf("Sum of two number is %d\n",c);
    printf("subtraction of two number is %d\n",d);
    printf("multiplication of two number is %d\n",e);
    printf("Division of two number %d\n",f);
    return 0;
}